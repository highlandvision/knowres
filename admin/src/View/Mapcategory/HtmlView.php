namespace HighlandVision\Component\Knowres\Administrator\View\MapCategory;

defined('_JEXEC') or die;

use Exception;
use HighlandVision\Component\Knowres\Administrator\Model\MapcategoryModel;
use HighlandVision\KR\Framework\KrMethods;
use HighlandVision\KR\Joomla\Extend\HtmlView as KrHtmlView;
use Joomla\CMS\Toolbar\ToolbarHelper;

use function defined;
use function strtolower;

/**
 * Edit a map category
 *
 * @since 1.0.0
 */
class HtmlView extends KrHtmlView
{
	/**
	 * Display the view
	 *
	 * @param   null  $tpl
	 *
	 * @throws Exception
	 * @since  1.0.0
	 * @return void
	 */
	public function display($tpl = null): void
	{
		/** @var MapcategoryModel $model */
		$model       = $this->getModel();
		$this->form  = $model->getForm();
		$this->item  = $model->getItem();
		$this->state = $model->getState();

		$this->checkVersions();
		$this->checkErrors();

		ToolbarHelper::title(KrMethods::plain('COM_KNOWRES_MAPCATEGORY_TITLE'), 'fas fa-layer-group knowres');
		$this->addFormToolbar(strtolower($this->getName()));
		$this->getFormAriaLabel();

		parent::display($tpl);
	}
}